import math

def somar(a, b) :
    return a + b

def subtrair(a, b) :
    return a - b
    
def dividir(a, b) :
    return a /b

def multiplicar(a, b) :
    return a * b
    
def potencia(a, b) :
    return a ** b

def raiz(a) :
    return math.sqrt(a)

print(somar(10, 20))
print(subtrair(10, 20))
print(dividir(10, 20))
print(multiplicar(10, 20))
print(potencia(2, 2))
print(raiz(9))


